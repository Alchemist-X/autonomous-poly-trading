function buildPrompt(context: RuntimeExecutionContext, config: OrchestratorConfig): string {
  return [
    "You are running a cloud Polymarket autonomous agent.",
    "Use the vendored repos as references only:",
    `- ${config.claudeWorkspaceDir}/polymarket-market-pulse`,
    `- ${config.claudeWorkspaceDir}/all-polymarket-skill`,
    `- ${config.claudeWorkspaceDir}/polymarket-trading-TUI`,
    "",
    "Output only valid JSON matching the TradeDecisionSet schema.",
    `Overview: ${JSON.stringify(context.overview)}`,
    `Positions: ${JSON.stringify(context.positions)}`
  ].join("\n");
}

