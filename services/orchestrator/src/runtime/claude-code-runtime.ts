async function runClaudeCommand(command: string, prompt: string): Promise<string> {
  return new Promise((resolve, reject) => {
    const child = spawn("/bin/sh", ["-lc", command], {
      env: {
        ...process.env,
        AUTOPOLY_PROMPT: prompt
      }
    });

    let stdout = "";
    let stderr = "";

    child.stdout.on("data", (chunk) => {
      stdout += String(chunk);
    });
    child.stderr.on("data", (chunk) => {
      stderr += String(chunk);
    });
    child.on("error", reject);
    child.on("close", (code) => {
      if (code === 0) {
        resolve(stdout.trim());
        return;
      }
      reject(new Error(stderr || `Claude command exited with code ${code}`));
    });

    child.stdin.write(prompt);
    child.stdin.end();
  });
}

export class ClaudeCodeRuntime implements AgentRuntime {
  readonly name = "claude-code-headless";

  constructor(private readonly config: OrchestratorConfig) {}

  async run(context: RuntimeExecutionContext): Promise<RuntimeExecutionResult> {
    if (!this.config.claudeCodeCommand) {
      return buildMockResult(context);
    }

    const prompt = buildPrompt(context, this.config);
    const stdout = await runClaudeCommand(this.config.claudeCodeCommand, prompt);
    const parsed = tradeDecisionSetSchema.parse(JSON.parse(stdout));

    return {
      decisionSet: parsed,
      promptSummary: "Claude Code headless execution against vendored Polymarket skills.",
      reasoningMd: "Reasoning is supplied directly by Claude output artifacts.",
      logsMd: stdout
    };
  }
}

